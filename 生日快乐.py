import turtle
import os
from turtle import *
lf = left
bd = backward
sh = setheading


def sheng(x,y):
   pu()
   goto(x,y)
   pd()
   width(10)
   pencolor('purple')
   left(45)
   fd(45)
   backward(22)
   rt(135)
   pu()
   for i in range(3):
      left(90)
      pd()
      forward(100)
      backward(100)
      pu()
      rt(90)
      if i!=2:
         fd(35)
   left(90)
   pu()
   fd(50)
   lf(90)
   fd(90)
   lf(180)
   pd()
   fd(90)
   pu()
   setheading(0)


def ri(x,y):
    width(10)
    pu()
    #goto(x,y)
    pencolor('red')
    for i in range(3):
        goto(x,y-40*i)
        pd()
        fd(80)
        rt(90)
        fd((2-i)*40)
        pu()
        lt(90)
    setheading(0)
    goto(x,y)
    pd()
    rt(90)
    fd(90)
    pu()
    setheading(0)


