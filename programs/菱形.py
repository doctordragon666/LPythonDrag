# 功能：打印菱形
# 目的：灵活处理字符串

def fun(b):
    o = '*'
    o = o * b
    print(o.center(50, ' '))


for i in range(1, 40, 5):
    fun(i)
for q in range(40, 0, -5):
    fun(q)

# 在python中运行的结果是：
#     *
#    ***
#   *****
#  *******
# *********
#  *******
#   *****
#    ***
#     *
